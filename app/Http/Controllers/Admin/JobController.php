    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
        $jobcategories = Jobcategory::all();
        $jobtypecategories = Jobtypecategory::all();
        
        return view('admin.job.create',compact('jobcategories','jobtypecategories'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $this->validate($request,[
            'company' => 'required',
            'job_title' => 'required',
            'category' => 'required',
            'type' => 'required',
            'salary' => 'required',
            'contact_email' => 'required',
            'phone' => 'required',
            'contact_user' => 'required',
            'location' => 'required',
            'description' => 'required',
        ]);
      
        $slug = str_slug($request->title);
        
        $job = new Job();
        $job->user_id = Auth::id();
        $job->company = $request->company;
        $job->job_title = $request->job_title;
        $job->slug = $slug;
        $job->salary = $request->salary;
        $job->phone = $request->phone;
        $job->contact_email = $request->contact_email;
        $job->location = $request->location;
        $job->contact_user = $request->contact_user;
        $job->salary = $request->salary;
        $job->description = $request->description;
        $job->jobcategory_id = $request->category;
        $job->jobtypecategory_id = $request->type;
        
        $job->save();

      
       
       
        Toastr::success('Job Added Successfully Saved :)','Success');
        return redirect()->route('admin.job.index');
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Post  $post
     * @return \Illuminate\Http\Response
     */
    public function show(Post $post)
